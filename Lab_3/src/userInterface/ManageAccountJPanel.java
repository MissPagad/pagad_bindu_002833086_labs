    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblAccountTable.getSelectedRow();
        
        if(selectedRowIndex >= 0){
             int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Would ypou like to delete account?", "Warning", dialogButton);
        if(dialogResult == dialogButton){
             Account selectedAccount = (Account) tblAccountTable.getValueAt(selectedRowIndex, 0);
             accountList.deleteAccount(selectedAccount);
             JOptionPane.showMessageDialog(this, "Account deleted.");
             populateTable();
        }
        }else{
         JOptionPane.showMessageDialog(this, "Please select a row to delete.");

        }
       
                   
        
    }//GEN-LAST:event_btnDeleteActionPerformed

