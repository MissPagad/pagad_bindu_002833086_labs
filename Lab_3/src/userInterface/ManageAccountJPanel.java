    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String getSerachedRoutingNumber = txtSearch.getText();
        Account result = accountList.searchAccount(getSerachedRoutingNumber);
        if(result == null){
       JOptionPane.showConfirmDialog(null, "Account number entered does not exists", "Warning", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
        ViewAccountJPanel  viewPanel = new ViewAccountJPanel(userProcessContainer, result, this);
        userProcessContainer.add("ViewAccountJPanel", viewPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
        
        
    }//GEN-LAST:event_btnSearchActionPerformed

